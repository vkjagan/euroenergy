                            <h2> Slope Stabilization</h2>
							
							<h3> Soil Nails & RockBolts</h3>
							
							<p>Soil Nailing has proven to be successful in arresting slope failures, which is famous in Hong Kong, Taiwan, Singapore and Malaysia. In this process, a failing hillside or earth mass is &ldquo;stapled&rdquo; in place by using inclined &ldquo;nail&rdquo; consisting a drilled grouted hole with a single piece of reinforcement bar. The nailing process creates a single block of earth able to hold-back its overburden. The slope equilibrium remains essentially undisturbed throughout the construction process of soil nailing, rendering it practical and useful to immediately arresting any slopes including potentially failing slopes. Soil nailing not only works in tension, but also bending and shearing forces, usually from the slip circle of slope. A reinforced surface is usually required over the soil nails, typically made of concrete (gunite) as a face cover, which is reinforced using woven mesh over steel plates. Our small portable drilling frames, also used for underpinning are best suited for soil nails as they can be easily inclined and held onto scaffolds and reaction frames anchored onto slope. The drilling method we use for short nails and dry soils is basically a drag bit with a powerful air compressor to blow soil particles out. Alternatively, a soil auger can be used. Sized available for these options are 100mm diameter. Typically the construction is top-down with an excavator to trim the slope prior to installation and our concrete (Guniting) to reinforce the slope with BRC and sprayed concrete after the installation of the soil nail. Depending on construction specifications, we can install different materials such as corrugated duct into soil nail prior to grouting or using galvanized nails to prevent corrosion. Rock bolts are similar to soil nails which resist forced by means of grouted steel bars embedded into bedrock. Construction sequence is similar to soil nails except that drilling is done using a smaller Down-the-Holy Hammer of diameter 80- 100mm.</p>
							
							<h3> Horizontal Drain</h3>
							
							<p>Often water pressure buildup in slope due to infiltration and rainfall are main causes of slope failures. Without adequate drainage and vegetation, the slopes may fail. The fast release of ground water is crucial for slope stability hence the idea of perforated horizontally with a slight upward tilt into slopes, allowing ground water to seep into it and drain away. This reduces the bulk weight of the slope, preventing potential sliding failure. Construction of horizontal drains is similar to soil nails, using our small portable drilling frame. However, drains are inclined upwards and our rigs are adjustable to suit the client specified angle. Typical diameter is 75mm to 100mm which can drill using various methods.</p>
							
